/**
 * Constants for the medevac IPC (Infection Prevention & Control) simulation module.
 *
 * Operational definitions are aligned with WHO/ECDC/AEMPS guidance for patient
 * transfer and transport, syndromic screening, and antimicrobial stewardship
 * in emergency evacuation contexts.
 */

/** Lifecycle states of an evacuated patient through the medevac workflow. */
export const EVAC_STATES = [
  'triaged',
  'awaiting_transport',
  'in_transit',
  'handover',
  'isolated',
  'cohorted',
  'cleared',
] as const
export type EvacState = (typeof EVAC_STATES)[number]

/**
 * Syndromic screening categories for pre-microbiological triage.
 * Follows WHO syndromic screening approach for early detection and safe placement.
 */
export const SYNDROME_CATEGORIES = [
  'respiratory',
  'diarrhoea',
  'rash',
  'other',
] as const
export type SyndromeCategory = (typeof SYNDROME_CATEGORIES)[number]

/** Types of simulation records captured during exercises. */
export const RECORD_TYPES = ['patient_event', 'ops_event'] as const
export type RecordType = (typeof RECORD_TYPES)[number]

/**
 * IPC alert types generated by the rule engine.
 * Each type maps to a distinct operational workflow (worklist, notification, or flag).
 */
export const ALERT_TYPES = [
  'isolation_flag',        // Patient requires isolation room/area
  'cohort_flag',           // Patient can be cohorted with similar cases
  'cleaning_worklist',     // Vehicle/area decontamination task
  'proa_continuity',       // Antimicrobial stewardship — dose window at risk
  'contact_trace',         // Contact tracing initiated for exposed personnel
  'route_change',          // Clean/dirty zone conflict — reroute required
  'screening_required',    // MDRO screening on arrival (transfrontal transfers)
] as const
export type AlertType = (typeof ALERT_TYPES)[number]

/**
 * Transmission-based precaution levels.
 * Mapped from syndrome/pathogen to operational measures per WHO/ECDC guidance.
 */
export const PRECAUTION_TYPES = [
  'standard',
  'contact',
  'droplet',
  'airborne',
  'droplet_contact',       // Combined (e.g., respiratory infections with secretions)
  'contact_enhanced',      // MDRO/CPE — gloves + gown + dedicated equipment
] as const
export type PrecautionType = (typeof PRECAUTION_TYPES)[number]

/**
 * Operational roles that receive and acknowledge IPC alerts.
 * Used to route worklists and measure alert-acknowledgement latency by role.
 */
export const ROLES = [
  'dispatch',
  'transport_crew',
  'receiving_nurse',
  'infection_control',
  'pharmacy_proa',
  'evs',                   // Environmental services / cleaning team
  'medical_officer',
] as const
export type Role = (typeof ROLES)[number]

/**
 * SLO targets (seconds) for alert delivery by priority class.
 * These are engineering/operational targets to be validated in simulations.
 * Critical: before embarkation or ≤ 120 s from new information.
 * High: within the dosing window (≤ 600 s).
 * Medium: trend/cluster monitoring (≤ 7200 s).
 */
export const ALERT_SLO_SECONDS: Record<'critical' | 'high' | 'medium', number> =
  {
    critical: 120,
    high: 600,
    medium: 7200,
  }

/**
 * Alert priority by type — drives SLO selection and UI interrupt behaviour.
 * Only 'critical' alerts trigger immediate interruption; others go to worklists.
 */
export const ALERT_PRIORITY: Record<AlertType, 'critical' | 'high' | 'medium'> =
  {
    isolation_flag: 'critical',
    cohort_flag: 'critical',
    route_change: 'critical',
    proa_continuity: 'high',
    screening_required: 'high',
    cleaning_worklist: 'medium',
    contact_trace: 'medium',
  }

/**
 * Default precaution recommended per syndrome category when pathogen is unknown.
 * Conservative mappings per WHO syndromic screening guidance for emergency settings.
 */
export const DEFAULT_PRECAUTION_BY_SYNDROME: Record<
  SyndromeCategory,
  PrecautionType
> = {
  respiratory: 'droplet_contact',
  diarrhoea: 'contact',
  rash: 'contact',
  other: 'standard',
}

/**
 * Chemical contact time (minutes) required for vehicle/area decontamination.
 * Sodium hypochlorite 0.1% is the WHO default for non-sporicidal surfaces.
 * Sporicidal (C. difficile) requires 0.5% for ≥ 10 min.
 */
export const DECON_CONTACT_TIME_MINUTES: Record<
  'standard' | 'sporicidal',
  number
> = {
  standard: 1,
  sporicidal: 10,
}
